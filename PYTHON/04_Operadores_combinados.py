
"""
||-------------------------||
||--OPERADORES COMBINADOS--||
||-------------------------||
||--------SUMA (+=)--------||
||-------RESTA (-=)--------||
||---MULTIPLICACIÓN (*=)---||
||-----EXPONENTE (**=)-----||
||------DIVISIÓN (/=)------||
||--DIVISIÓN ENTERA (//=)--||
||-------MODULO (%=)-------||
||-------------------------||
"""

i = 10

# SUMA 
i += 10   # i = i + 10
print(i)  # Imprime 20

# RESTA 
i -= 10   # i = i - 10
print(i)  # Imprime 10

# MULTIPLICACIÓN 
i *= 2    # i = i * 2
print(i)  # Imprime 20

# REESRIBIR VALOR EN MEMORIA
i = 10
# ELEVACIÓN 
i **= 10  # y = y + 10
print(i)  # Imprime 10000000000

i = 10
# DIVISIÓN 
i /= 2    # y = y + 10
print(i)  # Imprime 5.0

i = 10
# DIVISIÓN ENTERA 
i //= 2   # y = y + 10
print(i)  # Imprime 5

i = 10
# MODULO 
i %= 2    # y = y + 10
print(i)  # Imprime 0.0
