#INDEXACIÓN
#Indexación con coleciones

ropa = ["abrigo", "zapatos", "pantalones", "camisa"]
print(ropa[0])    # Imprime "abrigo"
print(ropa[2])    # Imprime "pantalones"
print(ropa[-1])   # Imprime "camisa"
print(ropa[-3])   # Imprime "zapatos"

#Indexación con cadenas de texto
cadena = "Cadena 100"
print(cadena[0])   # Imprime  C
print(cadena[6])   # Imprime ' '
print(cadena[-1])  # Imprime  0
